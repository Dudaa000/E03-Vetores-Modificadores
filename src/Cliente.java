public class Cliente{
    String nome;
    String CPF;
    String endereco;
    int idade;
    char sexo;

    public Cliente(String nome, String CPF, String endereco, int idade, char sexo) {
        this.nome = nome;
        this.CPF = CPF;
        this.endereco = endereco;
        this.idade = idade;
        this.sexo = sexo;
    }
}